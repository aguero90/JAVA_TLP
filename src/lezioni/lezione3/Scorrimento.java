package lezione3;

public class Scorrimento {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int i=8;
		int j=3;
		int k=-8;
		
		System.out.println("i >> j " + (i >>  j));
		System.out.println("i << j " + (i << j));
		System.out.println("k >> j " + (k >> j ));
		System.out.println("k >>> j " + (k >>> j ));

		
		
	} 

}
